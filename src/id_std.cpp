	QByteArray file_get_contents(const QString &src, bool *ok)
	{
		QByteArray ret;
		bool b;

		b = false;
		if(!src.isEmpty())
		{
			QFile file(src);
			file.open(QIODevice::ReadOnly);
			if(file.isOpen())
			{
				ret = file.readAll();
				file.close();
				b = true;
			}
		}

		if(ok)
			*ok = b;
		return ret;
	}
